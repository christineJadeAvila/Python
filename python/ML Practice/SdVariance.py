import numpy # import standard deviation, std() and variance()

# get Standard Deviation
speed = [86,87,88,86,87,85,86]

standardDeviation = numpy.std(speed)
print(standardDeviation)

speed = [32,111,138,28,59,77,97]

standardDeviation1 = numpy.std(speed)
print(standardDeviation1)

# get Variance

variance = numpy.var(speed)

print(variance)

